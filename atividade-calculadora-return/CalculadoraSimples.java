public class CalculadoraSimples {

    int somar (int a, int b) { //não precisa declarar variavel porque não quer guardar os valores
        return a + b; //usando quando precisa retornar mais vezes
    }
    int subtracao (int a, int b) {

        return a - b;
    }
    int multiplicacao (int a, int b) {

        return  a * b;
    }
    int divisao (int a, int b) {

        return a / b;
    }
}
